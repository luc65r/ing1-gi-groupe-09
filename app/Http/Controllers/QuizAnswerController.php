    /**
     * Store a newly created resource in storage.
     *
     * @param  \App\Http\Requests\StoreAnswerRequest  $request
     * @return \Illuminate\Http\Response
     */
    public function store(StoreAnswerRequest $request, Quiz $quiz)
    {
        $teamId = Auth::user()->student->teams()->where('project_id', $quiz->project->id)->first()->id;

        foreach ($request->input('reponses') as $questionId => $response) {

            Answer::create([
                'question_id' => $questionId,
                'team_id' => $teamId,
                'answer' => $response,
            ]);
        }

        return redirect()->route('quizzes.show', $quiz->id);
    }

